/**
 * setup-local-config.js
 * Runs automatically before `npm start` via the "prestart" script.
 * Reads .env and writes AiAssistControl/localConfig.ts so the PCF
 * test harness picks up your API key without hardcoding it anywhere.
 *
 * localConfig.ts is gitignored so it never gets committed.
 */

const fs = require("fs");
const path = require("path");

const envPath = path.resolve(__dirname, ".env");
const outputPath = path.resolve(__dirname, "AiAssistControl", "localConfig.ts");

// Parse .env manually
function parseEnv(filePath) {
	if (!fs.existsSync(filePath)) return {};
	return fs
		.readFileSync(filePath, "utf-8")
		.split("\n")
		.reduce((acc, line) => {
			const trimmed = line.trim();
			if (!trimmed || trimmed.startsWith("#")) return acc;
			const eqIndex = trimmed.indexOf("=");
			if (eqIndex === -1) return acc;
			const key = trimmed.slice(0, eqIndex).trim();
			const value = trimmed
				.slice(eqIndex + 1)
				.trim()
				.replace(/^["']|["']$/g, "");
			acc[key] = value;
			return acc;
		}, {});
}

const env = parseEnv(envPath);
const apiKey = env.ANTHROPIC_API_KEY || "";

if (!apiKey) {
	console.warn(
		"\n⚠  No ANTHROPIC_API_KEY found in .env — the AI Assist button won't work locally.\n" +
			"   Create a .env file in the project root with:\n\n" +
			"   ANTHROPIC_API_KEY=sk-ant-your-key-here\n",
	);
}

const output = `// AUTO-GENERATED by setup-local-config.js — do not edit manually
// This file is gitignored. Run \`npm start\` to regenerate from .env.
export const LOCAL_API_KEY = "${apiKey}";
`;

fs.writeFileSync(outputPath, output, "utf-8");
console.log("✓ Local config written from .env");
